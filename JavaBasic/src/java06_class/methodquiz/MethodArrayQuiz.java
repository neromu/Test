package java06_class.methodquiz;

public class MethodArrayQuiz {
	
	public void mul10(int[] arr) { // arr 참조를 받음
		for(int i=0; i<arr.length;i++) {
			arr[i] *= 10;
		}
	};
	
	
	public void print(int[] arr) {
		for(int i=0; i<arr.length;i++) {
			System.out.print(arr[i]+ " ");
		}
	};
	
	
	
	
	
	
	
	
	
	
	
	
	
}//class
